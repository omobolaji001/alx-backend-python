#!/usr/bin/env python3
""" A script of type-annoted function that takes a float
as an argument and returns string representation of the argument.
"""


def to_str(n: float) -> str:
    """ A function that converts a float to string

    Args:
            n (float): the floating number to be converted

    Returns:
            str: string representation of the float
    """
    return str(n)
